function drawBoard(board, ctx, DIM) {
  ctx.clearRect(0, 0, DIM.width * cellSize.width, DIM.height * cellSize.height);
  board.map((cell, i) => {
    if (cell == 1) {
      let x = i % DIM.width;
      let y = Math.floor(i / DIM.width);
      ctx.fillStyle = gray;
      ctx.fillRect(
        x * cellSize.width,
        y * cellSize.height,
        cellSize.width,
        cellSize.height
      );
    }
  });
}
function init() {
  drawBoard(plane, fourOfourCTX, DIM);
  player = new Player({
    cord: { x: 1, y: 2 },
    color: "blue",
    size: cellSize,
    ctx: fourOfourCTX,
    plane: plane,
    DIM: DIM,
  });
  player.leave = () => {
    container.classList.add("ended");
    setTimeout(() => {
      fadeOut.classList.add("active");
      if (innerWidth >= 500) {
        initMaze();
      } else {
        initPhoneMaze();
      }
      setTimeout(() => {
        firstSection.style.display = "none";
        mazeSection.style.display = "block";
        fadeOut.classList.remove("active");
      }, 1000);
    }, 1000);
  };
}
function initMaze() {
  drawBoard(maze, mazeCTX, mazeDIM);
  player = new Player({
    cord: { x: 1, y: 1 },
    color: "blue",
    size: cellSize,
    ctx: mazeCTX,
    plane: maze,
    DIM: mazeDIM,
  });
  player.leave = () => {
    let linkIndex;
    if (player.cord.y == 2) {
      mazeContainer.classList.add("high");
      linkIndex = 0;
    } else if (player.cord.y == 14) {
      mazeContainer.classList.add("middle");
      linkIndex = 1;
    } else if (player.cord.y == 25) {
      mazeContainer.classList.add("low");
      linkIndex = 2;
    }
    mazeContainer.classList.add("ended");

    setTimeout(() => {
      links[linkIndex].classList.add("hovered");
      setTimeout(() => {
        // Redirect
        window.location.assign(links[linkIndex].firstElementChild.href);
      }, 500);
    }, 250);
  };
}
