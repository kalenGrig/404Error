init();

document.addEventListener("keydown", (e) => {
  if (!player.isMoving) {
    switch (e.key) {
      case "ArrowLeft":
        if (player.possibleMoves.left) {
          player.left();
        }
        break;
      case "ArrowUp":
        if (player.possibleMoves.up) {
          player.up();
        }
        break;
      case "ArrowRight":
        if (player.possibleMoves.right) {
          player.right();
        }
        break;
      case "ArrowDown":
        if (player.possibleMoves.down) {
          player.down();
        }
        break;
    }
  }
});

document.addEventListener("touchstart", (e) => {
  xDown = e.touches[0].clientX;
  yDown = e.touches[0].clientY;
});

document.addEventListener("touchend", (e) => {
  if (!xDown || !yDown) {
    return;
  }

  let xUp = e.changedTouches[0].clientX;
  let yUp = e.changedTouches[0].clientY;

  let xDiff = xDown - xUp;
  let yDiff = yDown - yUp;
  if (!player.isMoving) {
    if (Math.abs(xDiff) > Math.abs(yDiff)) {
      if (xDiff > 0) {
        player.left();
      } else {
        player.right();
      }
    } else {
      if (yDiff > 0) {
        player.up();
      } else {
        player.down();
      }
    }
  }
  xDown = null;
  yDown = null;
});
